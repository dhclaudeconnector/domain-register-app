import { existsSync, readFileSync, writeFileSync } from 'node:fs';
import { randomBytes } from 'node:crypto';
import { readConfigApp, readServiceAccount } from './firebase-common.mjs';

const envPath = '.env.local';
const force = process.argv.includes('--force');
const { path, serviceAccount } = readServiceAccount();
const { path: configPath, configApp } = readConfigApp();
const projectId = serviceAccount.project_id;
const existing = existsSync(envPath) ? readFileSync(envPath, 'utf8') : '';
const values = new Map();

for (const line of existing.split(/\r?\n/)) {
  if (!line || line.trim().startsWith('#') || !line.includes('=')) continue;
  const index = line.indexOf('=');
  values.set(line.slice(0, index), line.slice(index + 1));
}

function setDefault(key, value) {
  if (force || !values.get(key)) values.set(key, value);
}

setDefault('NEXT_PUBLIC_FIREBASE_API_KEY', configApp?.apiKey || '');
setDefault('NEXT_PUBLIC_FIREBASE_AUTH_DOMAIN', configApp?.authDomain || `${projectId}.firebaseapp.com`);
setDefault('NEXT_PUBLIC_FIREBASE_PROJECT_ID', configApp?.projectId || projectId);
setDefault('NEXT_PUBLIC_FIREBASE_DATABASE_URL', configApp?.databaseURL || serviceAccount.databaseURL);
setDefault('NEXT_PUBLIC_FIREBASE_STORAGE_BUCKET', configApp?.storageBucket || `${projectId}.firebasestorage.app`);
setDefault('NEXT_PUBLIC_FIREBASE_MESSAGING_SENDER_ID', configApp?.messagingSenderId || '');
setDefault('NEXT_PUBLIC_FIREBASE_APP_ID', configApp?.appId || '');
setDefault('NEXT_PUBLIC_ENCRYPT_SALT', serviceAccount.secret01 || randomBytes(32).toString('hex'));
setDefault('FIREBASE_SERVICE_ACCOUNT_PATH', path.replaceAll('\\', '/'));
setDefault('FIREBASE_CONFIG_APP_PATH', configPath.replaceAll('\\', '/'));
setDefault('FIREBASE_REGION', 'asia-southeast1');

const ordered = [
  '# Generated by npm run firebase:env',
  '# Fill web-only values from Firebase Console > Project settings > Your apps > Web app.',
  'NEXT_PUBLIC_FIREBASE_API_KEY',
  'NEXT_PUBLIC_FIREBASE_AUTH_DOMAIN',
  'NEXT_PUBLIC_FIREBASE_PROJECT_ID',
  'NEXT_PUBLIC_FIREBASE_DATABASE_URL',
  'NEXT_PUBLIC_FIREBASE_STORAGE_BUCKET',
  'NEXT_PUBLIC_FIREBASE_MESSAGING_SENDER_ID',
  'NEXT_PUBLIC_FIREBASE_APP_ID',
  'NEXT_PUBLIC_ENCRYPT_SALT',
  '',
  '# JSON fallback paths. Used only when explicit env values are empty.',
  'FIREBASE_SERVICE_ACCOUNT_PATH',
  'FIREBASE_CONFIG_APP_PATH',
  'FIREBASE_REGION',
];

const orderedKeys = new Set(ordered.filter((key) => key && !key.startsWith('#')));
const extra = [...values.keys()].filter((key) => !orderedKeys.has(key)).sort();
const output = [
  ...ordered.map((key) => {
    if (!key || key.startsWith('#')) return key;
    return `${key}=${values.get(key) || ''}`;
  }),
  ...(extra.length ? ['', '# Preserved existing values.', ...extra.map((key) => `${key}=${values.get(key) || ''}`)] : []),
].join('\n') + '\n';

writeFileSync(envPath, output);

const missing = ['NEXT_PUBLIC_FIREBASE_API_KEY', 'NEXT_PUBLIC_FIREBASE_MESSAGING_SENDER_ID', 'NEXT_PUBLIC_FIREBASE_APP_ID'].filter((key) => !values.get(key));
console.log(`Wrote ${envPath} from ${path}`);
if (missing.length) {
  console.log(`Missing web-app-only values: ${missing.join(', ')}`);
}
